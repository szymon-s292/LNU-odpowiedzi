def chineseWeekDays(chineseDays, day):
    if day == "monday":
        return chineseDays[0]
    elif day == "tuesday":
        return chineseDays[1]
    elif day == "wednesday":
        return chineseDays[2]
    elif day == "thursday":
        return chineseDays[3]
    elif day == "friday":
        return chineseDays[4]
    elif day == "saturday":
        return chineseDays[5]
    elif day == "sunday":
        return chineseDays[6]